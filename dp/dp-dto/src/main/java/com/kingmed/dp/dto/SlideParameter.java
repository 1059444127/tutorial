package com.kingmed.dp.dto;

/**
 * 实验室系统请求-数字切片的参数
 * @author zhengjunjie
 *
 */
public class SlideParameter{
	private String ns_request_id;
	private String ns_request_code;
	private String ns_name;
	private String ns_url;
	private String ns_vender;
	private String ns_antibody;
	private String ns_img_url;
	
	public String getNs_request_id() {
		return ns_request_id;
	}
	public void setNs_request_id(String ns_request_id) {
		this.ns_request_id = ns_request_id;
	}
	public String getNs_request_code() {
		return ns_request_code;
	}
	public void setNs_request_code(String ns_request_code) {
		this.ns_request_code = ns_request_code;
	}
	public String getNs_name() {
		return ns_name;
	}
	public void setNs_name(String ns_name) {
		this.ns_name = ns_name;
	}
	public String getNs_url() {
		return ns_url;
	}
	public void setNs_url(String ns_url) {
		this.ns_url = ns_url;
	}
	public String getNs_vender() {
		return ns_vender;
	}
	public void setNs_vender(String ns_vender) {
		this.ns_vender = ns_vender;
	}
	public String getNs_antibody() {
		return ns_antibody;
	}
	public void setNs_antibody(String ns_antibody) {
		this.ns_antibody = ns_antibody;
	}
	public String getNs_img_url() {
		return ns_img_url;
	}
	public void setNs_img_url(String ns_img_url) {
		this.ns_img_url = ns_img_url;
	}
	
	
}
